    private void enregister2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enregister2ActionPerformed
        
        try{
            Connexion con = new Connexion();
            Statement st = con.connect().createStatement();
            if (num.getText().equals("") || cin1.getText().equals("") || datedebut.getText().equals("") || dateretour.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Le remplissage de tout les champs est obligatoire!","Erreur",JOptionPane.ERROR_MESSAGE);
            }
            else{
                int mat = Integer.parseInt(num.getText());
                int cin = Integer.parseInt(cin1.getText());
                String Dated = datedebut.getText();
                String Dater = dateretour.getText();
                
                ResultSet rs = st.executeQuery("SELECT etat FROM vehicule WHERE NumMatricule='"+mat+"'");
                if (!rs.next()){
                    JOptionPane.showMessageDialog(null,"Le véhicule n'existe pas!","Erreur",JOptionPane.ERROR_MESSAGE);
                }
                else{
                    if(rs.getString(1).equals("Réservé")){
                        JOptionPane.showMessageDialog(null,"Le vehicule est déja réservé");
                    }
                    else{
                        rs = st.executeQuery("SELECT * FROM client WHERE CIN='"+cin+"'");
                        if (rs.next()==false){
                            JOptionPane.showMessageDialog(null,"Le client n'existe pas","Erreur",JOptionPane.ERROR_MESSAGE);
                        }
                        else{                           
                            rs = st.executeQuery("SELECT CIN_Client FROM reservation WHERE CIN_Client='"+cin+"'");
                            if (rs.next()){
                                JOptionPane.showMessageDialog(null,"Le client a déja une réservation courante","Erreur",JOptionPane.ERROR_MESSAGE);
                            }
                            else{
                                st.executeUpdate("INSERT INTO reservation VALUES(Num_res,'"+cin+"','"+mat+"',DATE '"+Dated+"',DATE '"+Dater+"');");
                                JOptionPane.showMessageDialog(null,"Réservation ajouté!","Ajout",JOptionPane.INFORMATION_MESSAGE);
                                st.executeUpdate("UPDATE vehicule SET etat='Réservé' WHERE NumMatricule='"+mat+"'");
                            }
                        }
                    }
                }
            }
                
   
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_enregister2ActionPerformed
    public long daysbetween(Date one, Date two){
        long diff = (one.getTime()-two.getTime())/86400000;
        return Math.abs(diff);
    }
    public Date formatDate(String date){
        String y = date.substring(0,date.indexOf("-"));
        String m = date.substring(date.indexOf("-")+1,date.lastIndexOf("-")-1);
        String d = date.substring(date.lastIndexOf("-")+1,date.length()-1);
        int year = Integer.parseInt(y);
        int month = Integer.parseInt(m);
        int day = Integer.parseInt(d);
        Date out = new Date(year,month,day);
        return out;
    }
    
    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        try{
            int id = Integer.parseInt(JOptionPane.showInputDialog("Donnez le numéro de la réservation à supprimer"));
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            Connexion con = new Connexion();
            Statement st = con.connect().createStatement();
            ResultSet rs = st.executeQuery("SELECT Date_Retour, NumMatricule FROM reservation WHERE Num_res='"+id+"'");
            if (rs.next()){
                int mat = rs.getInt(2);
                Date retour = formatDate(rs.getDate(1).toString());
                LocalDateTime now = LocalDateTime.now();
                Date current = formatDate(dtf.format(now));
                if (retour.compareTo(current) > 0){
                    String options[] = {"Oui","Non"};
                    int choix = JOptionPane.showOptionDialog(null, "La date de retour de cette réservation n'a pas encore arrivé! Vous etes sur de votre décision?","Avertissement",JOptionPane.DEFAULT_OPTION,JOptionPane.INFORMATION_MESSAGE,null,options,options[0]);
                    if (choix == 0){
                        st.executeUpdate("DELETE FROM reservation WHERE Num_res='"+id+"'");
                        st.executeUpdate("UPDATE vehicule SET etat='Disponible' WHERE NumMatricule='"+mat+"'");
                        JOptionPane.showMessageDialog(null,"Réservation supprimée!","Suppression",JOptionPane.INFORMATION_MESSAGE);
                    }
                }
                else{
                    st.executeUpdate("DELETE FROM reservation WHERE Num_res='"+id+"'");
                    st.executeUpdate("UPDATE vehicule SET etat='Disponible' WHERE NumMatricule='"+mat+"'");
                    JOptionPane.showMessageDialog(null,"Réservation supprimée!","Suppression",JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_supprimerActionPerformed

