    private void editerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editerActionPerformed
        try{
            String deb = JOptionPane.showInputDialog("Donnez la nouvelle date de debut (AAAA-MM-JJ)");
            String fin = JOptionPane.showInputDialog("Donnez la nouvelle date de retour (AAAA-MM-JJ)");
            Connexion con = new Connexion();
            Statement st = con.connect().createStatement();
            int id = Integer.parseInt(JOptionPane.showInputDialog("Donnez le numero de la réservation dont vous voulez modifier"));
            ResultSet rs = st.executeQuery("SELECT * FROM reservation where num_res='"+id+"'");
            if(rs.next()){
                st.executeUpdate("UPDATE reservation SET Date_Debut='"+deb+"', Date_Retour='"+fin+"' WHERE num_res='"+id+"'");
                JOptionPane.showMessageDialog(null,"Réservation modifiée!","Modification",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(null,"La réservation n'existe pas!","Erreur",JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_editerActionPerformed

